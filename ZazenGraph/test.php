    $this->r = $r;
    $this->i = $i;
  }

  function getReal()
  {
    return $this->r;
  }
  function getImag()
  {
    return $this->i;
  }
  function getAbs()
  {
    return sqrt($this->r * $this->r + $this->i * $this->i);
  }

  function addFloat(float $v)
  {
    $this->r += $v;
  }
  function addComplex(Complex $v)
  {
    $this->r += $v->getReal();
    $this->i += $v->getImag();
  }

